#!/usr/bin/env bash
# wait-for-it.sh: Wait for a service to be available before executing a command
# Usage: ./wait-for-it.sh host:port [-t timeout] [-- command args]

set -e

TIMEOUT=15
QUIET=0
HOST=""
PORT=""
CMD=""

usage() {
    echo "Usage: $0 host:port [-t timeout] [-q] [-- command args]"
    echo "  -t TIMEOUT          Timeout in seconds, default is 15"
    echo "  -q                  Quiet mode"
    echo "  host:port           Host and port to wait for"
    echo "  -- COMMAND ARGS     Command with args to execute after host is available"
    exit 1
}

wait_for() {
    if [ "$QUIET" -ne 1 ]; then echo "Waiting for $HOST:$PORT..."; fi
    
    for i in $(seq $TIMEOUT); do
        if nc -z "$HOST" "$PORT" > /dev/null 2>&1; then
            if [ "$QUIET" -ne 1 ]; then echo "$HOST:$PORT is available after $i seconds"; fi
            return 0
        fi
        sleep 1
    done
    
    echo "Timeout occurred after waiting $TIMEOUT seconds for $HOST:$PORT"
    return 1
}

# Parse arguments
while [ $# -gt 0 ]; do
    case "$1" in
        -t)
            TIMEOUT="$2"
            shift 2
            ;;
        -q)
            QUIET=1
            shift 1
            ;;
        --)
            shift
            CMD="$@"
            break
            ;;
        *)
            if [ -z "$HOST" ]; then
                HOST=$(echo "$1" | cut -d: -f1)
                PORT=$(echo "$1" | cut -d: -f2)
                shift 1
            else
                usage
            fi
            ;;
    esac
done

if [ -z "$HOST" ] || [ -z "$PORT" ]; then
    usage
fi

wait_for

if [ -n "$CMD" ]; then
    exec $CMD
fi

exit 0

